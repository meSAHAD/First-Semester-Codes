#include <stdio.h>
#include <stdlib.h>

int main()
{
    /* This program shows if a number is odd or even*/

    int num;
    printf("Enter your number\n ");
    scanf("%d",&num);

    if
        (num%2==0)
        printf("This number is even");
    else
        printf("Ths number is odd");
    return 0;
}
